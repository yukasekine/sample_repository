<?php
// 以下をfor文を使用して表示してください。
// ヒント: forの中でforを３回使用

// ********1
// *******121
// ******12321
// *****1234321
// ****123454321
// ***12345654321
// **1234567654321
// *123456787654321
// 12345678987654321
for($i = 1; $i <= 9; $i++){
  for($j = 8; $j >= $i; $j--){
    echo "*";
  }
  for($j = 1; $j <= $i; $j++){
    echo $j;
  }
  for($j = $i; $j > 1; $j--){
    echo $j-1;
  }
  echo "<br>";
}
?>